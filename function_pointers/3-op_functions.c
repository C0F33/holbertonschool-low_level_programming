#include <stdio.h>
#include <stdlib.h>
#include <string.h>
/**
 * op_add- addition
 * @a: Number to be calculated
 * @b: Number to be calculated
 * Return: Result
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
 * * op_sub- subtraction
 * @a: Number to be calculated
 * @b: Number to be calculated
 * Return: Result
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul- multiplication
 * @a: Number to be calculated
 * @b: Number to be calculated
 * Result: product
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * * op_div- division
 * @a: Number to be calculated
 * @b: Number to be calculated
 * Return: Result
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod- module
 * @a: Number to be calculated
 * @b: Number to be calculated
 * Result: residue
*/
int op_mod(int a, int b)
{
return (a % b);
}
