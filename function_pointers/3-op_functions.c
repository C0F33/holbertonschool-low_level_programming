#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - function does add
 * @a: int passed
 * @b: int passed
 * Return: math
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - function does sub
 * @a: int passed
 * @b: int passed
 * Return: math
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - function does mul
 * @a: int passed
 * @b: int passed
 * Return: math
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - function does div
 * @a: int passed
 * @b: int passed
 * Return: math
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - function does modulus
 * @a: int passed
 * @b: int passed
 * Return: math
 */
int op_mod(int a, int b)
{
return (a % b);
}
